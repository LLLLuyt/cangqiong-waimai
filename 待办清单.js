// ====== 待办清单 ======
//
// 你的任务：从 0 开始，写出一个完整的待办清单应用
//
// 功能要求：
// 1. 输入待办 → 点添加 → 新 <p> 出现在卡片里
// 2. 输入为空时点添加 → alert 提示
// 3. 点删除最后 → 删除最后一个待办
// 4. 顶部显示 "共 X 项"，添加 +1，删除 -1
//
// ====== 步骤提示 ======
//
// 1. 用 let 选中所有需要的元素（输入框、按钮、卡片、计数区）
//
// 2. 声明计数变量：let 计数 = 0;
//
// 3. 写 添加待办 函数：
//    - 判断输入是否为空 → alert
//    - 不为空 → createElement("p") → 设 textContent → appendChild → 计数+1 → 更新计数区
//
// 4. 写 删除最后一个 函数：
//    - querySelectorAll("p") 拿所有 p
//    - 取最后一个 → .remove()
//    - 计数 -1 → 更新计数区
//    - 注意：计数区也是 p 标签，取最后一个 p 时要排除它
//
// 5. 绑定事件：添加按钮 click、删除按钮 click
//
// ====== 你的代码： ======

// ╔══════════════════════════════════════════════╗
// ║  第 1 层：弹药库 — 选元素 + 声明变量          ║
// ║  这些变量像「遥控器」，后面所有操作都靠它们    ║
// ╚══════════════════════════════════════════════╝

let 清单卡片 = document.querySelector("#清单卡片");
// ↑ 拿到卡片容器 → 第 43 行 appendChild 往里面塞东西

let 待办输入 = document.querySelector("#待办输入");
// ↑ 拿到输入框 → 第 38 行 .value 读用户写了啥 → 第 42 行赋值给 p

let 添加按钮 = document.querySelector("#添加按钮");
// ↑ 拿到添加按钮 → 第 48 行绑 click 事件 → 触发 添加待办()

let 删除按钮 = document.querySelector("#删除最后按钮");
// ↑ 拿到删除按钮 → 第 59 行绑 click 事件 → 触发 删除最后一个()

let 计数显示 = document.querySelector("#计数显示");
// ↑ 拿到计数区 → 第 45 行 & 第 56 行 .textContent 刷新数字

let 计数 = 0;
// ↑ 大脑：记住现在有几项
//   第 44 行 +1 → 第 55 行 -1 → 第 45/56 行把值显示到页面

// ╔══════════════════════════════════════════════╗
// ║  第 2 层：添加功能 — 从输入到页面             ║
// ╚══════════════════════════════════════════════╝

function 添加待办() {
//  ↑ 函数名 → 第 48 行 addEventListener 引用它，点击时触发

    if (待办输入.value === "") {
    //  ↑ 用第 31 行的遥控器读输入框内容 → 空就进这个分支

        alert("请输入待办内容");
        // ↑ 弹窗提醒 → 然后什么都不做，函数结束

    } else {
    // ↑ 不为空进这个分支

        let p = document.createElement("p");
        // ↑ 造一个新 <p> 标签（还在内存里，页面还看不到）

        p.textContent = 待办输入.value;
        // ↑ 把输入框的文字装进 p 里
        //   左边 p.textContent → 要显示的
        //   右边 待办输入.value → 用户写的

        清单卡片.appendChild(p);
        // ↑ 把 p 塞进卡片 → 页面出现了！
        //   遥控器(清单卡片) → 第 30 行拿到的卡片容器

        计数 = 计数 + 1;
        // ↑ 大脑数字 +1（变量变了 ≠ 页面变了）

        计数显示.textContent = "共" + 计数 + " 项";
        // ↑ 把新数字刷到页面上
        //   遥控器(计数显示) → 第 34 行拿到的计数区
    }
}

添加按钮.addEventListener("click", 添加待办);
// ↑ 遥控器(添加按钮) ← 第 32 行拿到的
//   "click" → 左键单击
//   添加待办 → 第 37 行的函数，不加 () 因为要等点击才执行

// ╔══════════════════════════════════════════════╗
// ║  第 3 层：删除功能 — 从页面移除               ║
// ╚══════════════════════════════════════════════╝

function 删除最后一个() {
//  ↑ 函数名 → 第 59 行 addEventListener 引用它

    if (计数 > 0) {
    //  ↑ 门卫：有东西才让进
    //    计数 = 0 时直接跳过，防止误删计数器

        let 所有p = document.querySelectorAll("p");
        // ↑ 捞页面上所有 <p>：计数器 p + 待办 p 们

        let p = 所有p[所有p.length - 1];
        // ↑ 取最后一个的下标 = 长度 - 1
        //   例：3 个 p → length=3 → [2] 是最后一个

        p.remove();
        // ↑ 删掉自己（从页面消失）

        计数 = 计数 - 1;
        // ↑ 大脑数字 -1

        计数显示.textContent = "共" + 计数 + " 项";
        // ↑ 刷新计数显示（和第 45 行完全一样的操作）
    }
}

删除按钮.addEventListener("click", 删除最后一个);
// ↑ 遥控器(删除按钮) ← 第 33 行拿到的
//   点击 → 触发 删除最后一个()

