// ====== 第 1 层：选元素（遥控器） ======

let 计数显示 = document.querySelector("#计数显示");  // 计数器文字
let 文本框   = document.querySelector("#文本框");    // 输入框
let 添加     = document.querySelector("#添加");      // 添加按钮
let 删除     = document.querySelector("#删除");      // 删除按钮
let 卡片     = document.querySelector("#卡片");      // 卡片容器
let 计数     = 0;                                     // 玩家数量


// ====== 第 2 层：添加战绩 ======

function 添加战绩() {

    if (文本框.value === "") {                         // ← 守卫：空输入
        alert("请输入玩家名字战绩");                    //    弹窗提醒，然后啥也不做
        }                                        //    提前结束
    else{
    // ↓ 以下是 else 分支（不为空才执行）

    // 1. 创建段落 <p>
    let p = document.createElement("p");              // 造一个 <p> 标签（还在内存里）
    p.classList.add("玩家分数");                       // 穿衣服 — 对应 CSS 的 .玩家分数
    p.textContent = 文本框.value + ":0 分";           // 设文字，如「张三：0 分」

    // 2. 创建按钮 <button>+1</button>
    let 加按钮 = document.createElement("button");    // 造一个按钮（还在内存里）
    加按钮.textContent = "+1";                         // 按钮上写 +1

    // 3. 给按钮绑点击事件 — 函数直接写在里面，不用起名
    加按钮.addEventListener("click", function() {     // function() { 开了括号
        alert("给 " + 文本框.value + " 加了 1 分！");  //    弹窗提示
    });                                                // } = 函数结束  ) = 括号关闭  ; = 语句尾

    // 4. 组装 — 把按钮塞进 p，把 p 塞进卡片
    p.appendChild(加按钮);                             // p 肚子里装按钮
    卡片.appendChild(p);                               // 卡片肚子里装 p

    // 5. 更新计数
    计数 = 计数 + 1;                                   // 大脑数字 +1
    计数显示.textContent = "共 " + 计数 + " 个玩家";   // 刷新页面数字
}
}

添加.addEventListener("click", 添加战绩);             // 绑定：点添加 → 跑 添加战绩


// ====== 第 3 层：删除战绩 ======

function 删除战绩() {

    if (计数 > 0) {                                    // ← 守卫：没玩家不让删
        let 所有p = 卡片.querySelectorAll("p");        // 只在卡片里找 <p>
        let 最后一个 = 所有p[所有p.length - 1];         // 取数组最后一个
        最后一个.remove();                               // 删掉它

        计数 = 计数 - 1;                               // 大脑数字 -1
        计数显示.textContent = "共 " + 计数 + " 个玩家"; // 刷新页面数字
    }
}

删除.addEventListener("click", 删除战绩);             // 绑定：点删除 → 跑 删除战绩


// ====== 结构图 ======
//
//   页面 structure：
//   ┌─────────────────────────┐
//   │  卡片（#卡片）            │
//   │  ┌─────────────────────┐ │
//   │  │ 共 X 个玩家（计数显示）│ │  ← 写死在 HTML
//   │  └─────────────────────┘ │
//   │  ┌─────────────────────┐ │
//   │  │ 张三：0 分  [+1]     │ │  ← JS 动态创建（p 包 button）
//   │  └─────────────────────┘ │
//   │  ┌─────────────────────┐ │
//   │  │ 李四：0 分  [+1]     │ │  ← JS 动态创建
//   │  └─────────────────────┘ │
//   └─────────────────────────┘
//
//   嵌套关系：
//   卡片 > p（段落）
//          > button（加按钮）
//
//   事件绑定：
//   添加按钮（HTML 写死） →  绑定 添加战绩()
//   删除按钮（HTML 写死） →  绑定 删除战绩()
//   [+1]按钮（JS 创建）   →  绑定 function() { alert(...) }
